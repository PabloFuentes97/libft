#include "libft.h"

char	*ft_strrchr(char *str, int c)
{
	int		cont;

	cont = ft_strlen(str);
	while (cont >= 0)
	{
		if (str[cont] == c)
			return (&str[cont]);
		cont--;
	}
	return (0);
}
